function readInitialLanguage() {
  if (typeof window === 'undefined') return 'ru';
  const saved = window.localStorage.getItem('reserva-language');
  return dictionaries[saved] ? saved : 'ru';
}

function getValue(language, key) {
  return key.split('.').reduce((acc, part) => acc?.[part], dictionaries[language]) ??
    key.split('.').reduce((acc, part) => acc?.[part], dictionaries.ru) ??
    key;
}

const I18nContext = createContext(null);

export function I18nProvider({ children }) {
  const [language, setLanguageState] = useState(readInitialLanguage);

  const setLanguage = (nextLanguage) => {
    const safeLanguage = dictionaries[nextLanguage] ? nextLanguage : 'ru';
    setLanguageState(safeLanguage);
    window.localStorage.setItem('reserva-language', safeLanguage);
    document.documentElement.lang = safeLanguage;
  };

  const value = useMemo(() => ({
    language,
    languages,
    setLanguage,
    t: (key) => getValue(language, key),
  }), [language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) throw new Error('useI18n must be used within I18nProvider');
  return context;
}
